static void xregw(struct snd_soc_codec *codec, int addr, int val)
{
	u8 xbuf[2];
	xbuf[0] = addr & 0x0FF;
	xbuf[1] = val & 0x0FF;
	codec->hw_write(codec, xbuf, 2);
}
static int xregr(struct snd_soc_codec *codec, int addr)
{
	return codec->hw_read(codec, addr & 0x0FF);
}
void apply_patch_d_b1(struct snd_soc_codec *codec, reg_value *patch_dat, int patch_size)
{
	u8 val;
	int count;
	int dac_status;

	mutex_lock(&codec_io_mutex);

	xregw(codec, 0, 0); // page 0
	xregw(codec, 0x7F, 0x00); // book 0
	dac_status = xregr(codec, DAC_FLAG_R1);

	if((dac_status & 0x88) )
    {
	for(count=0; count < patch_size; count++)
		{
		xregw(codec, patch_dat[count].reg_off, patch_dat[count].reg_val);
		//printk("reg(%x) = %x\n",patch_dat[count].reg_off, xregr(codec, patch_dat[count].reg_off));
		}

	xregw(codec, 0, 0); // page 0
		xregw(codec, 0x7F, 0x50); // book 80
	val = xregr(codec, 0x01);
	val |= 1;
	xregw(codec, 0x01, val); // reg 1, do buf swap
		count = 0;

		do
		{
		val = xregr(codec, 0x01);
		count++;
		mdelay(1);
		//printk("val =%x\n", val);
		}while( (val & 0x1) && (count <= 100) );

		//printk("count=%d\n", count);

		for(count=0; count < patch_size; count++)
		{
		xregw(codec, patch_dat[count].reg_off, patch_dat[count].reg_val);
		//printk("reg(%x) = %x\n",patch_dat[count].reg_off, xregr(codec, patch_dat[count].reg_off));
		//mdelay(1);
		}
		//printk("DAC ENABLE\n");
    }
    else
    {
	for(count=0; count < patch_size; count++)
		{
		xregw(codec, patch_dat[count].reg_off, patch_dat[count].reg_val);
		//printk("reg(%x) = %x\n",patch_dat[count].reg_off, xregr(codec, patch_dat[count].reg_off));
		}

    }

	// restore to book 0 page 0
	xregw(codec, 0, 0); // page 0
	xregw(codec, 0x7F, 0x00); // book 0
	mutex_unlock(&codec_io_mutex);
}


